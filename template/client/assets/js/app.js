function getRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function generateRandomProducts(count = 100) {
  const products = [];

  for (let i = 0; i < count; i++) {
    const product = {
      user_id: getRandom(users),
      product_id: getRandom(categoryIds),
      content: getRandom(contents),
    };
    products.push(product);
  }

  return products;
}

